    private void btnGuardarMascotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarMascotaActionPerformed
        // TODO add your handling code here:
        try {

            if(txtNombreMascota.getText().trim().isEmpty() ||
               txtEspecie.getText().trim().isEmpty() ||
               txtRaza.getText().trim().isEmpty() ||
               txtEdad.getText().trim().isEmpty()) {

                javax.swing.JOptionPane.showMessageDialog( this,"Todos los campos son obligatorios");

                return;
            }

            int edad = Integer.parseInt(txtEdad.getText());

            String registro = txtNombreMascota.getText() + "," + txtEspecie.getText() + "," + txtRaza.getText() + "," + edad + cmbCliente.getSelectedItem();

            datos.ArchivoManager.guardar( "mascotas.txt",registro);

            javax.swing.JOptionPane.showMessageDialog(this,"Mascota registrada correctamente");
            
      

    } catch(NumberFormatException e){

        javax.swing.JOptionPane.showMessageDialog(this,"La edad debe ser un número");

    } catch(Exception e){

        javax.swing.JOptionPane.showMessageDialog(this,"Error al guardar");

    }
    }//GEN-LAST:event_btnGuardarMascotaActionPerformed

