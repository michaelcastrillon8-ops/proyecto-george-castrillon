    private void cargarClientes() {

    try {

        java.io.BufferedReader br = new java.io.BufferedReader( new java.io.FileReader("clientes.txt"));

        String linea;

        while((linea = br.readLine()) != null){

            String[] datos = linea.split(",");

            cmbCliente.addItem(datos[1]);

        }

        br.close();

    } catch(Exception e){

        System.out.println("No se pudieron cargar clientes");

    }
}

