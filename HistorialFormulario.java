    private void btnGuardarHistorialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarHistorialActionPerformed
        // TODO add your handling code here:
        try {

            if(cmbMascota.getSelectedIndex() == 0 ||
               txtFecha.getText().trim().isEmpty() ||
               txtDescripcion.getText().trim().isEmpty()) {

                javax.swing.JOptionPane.showMessageDialog(this,"Todos los campos son obligatorios");

                return;
            }

             String registro = cmbMascota.getSelectedItem().toString() + "," + txtFecha.getText() + "," + txtDescripcion.getText();

            datos.ArchivoManager.guardar("historial.txt",registro);

            javax.swing.JOptionPane.showMessageDialog(this, "Historial guardado correctamente");

    } catch(Exception e){

            javax.swing.JOptionPane.showMessageDialog(this,"Error al guardar historial");
        }
    }//GEN-LAST:event_btnGuardarHistorialActionPerformed

