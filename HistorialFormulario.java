    private void cargarMascotas() {

    try {

        java.io.BufferedReader br = new java.io.BufferedReader(new java.io.FileReader("mascotas.txt"));

        String linea;

        while((linea = br.readLine()) != null){

            String[] datos = linea.split(",");

            cmbMascota.addItem(datos[0]);

        }

        br.close();

    } catch(Exception e){

        System.out.println("No se pudieron cargar mascotas");

    }
}

